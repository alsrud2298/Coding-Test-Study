(SELECT DATE_FORMAT(sales_date,'%Y-%m-%d') as SALES_DATE
    , PRODUCT_ID
    , USER_ID
    , SALES_AMOUNT
FROM ONLINE_SALE
WHERE sales_date BETWEEN '2022-03-01 00:00:00' AND '2022-03-31 23:59:59')

UNION

(SELECT DATE_FORMAT(sales_date,'%Y-%m-%d') as SALES_DATE
    , PRODUCT_ID
    , NULL AS USER_ID
    , SALES_AMOUNT
FROM OFFLINE_SALE
WHERE sales_date BETWEEN '2022-03-01 00:00:00' AND '2022-03-31 23:59:59')

ORDER BY SALES_DATE, PRODUCT_ID, USER_ID
